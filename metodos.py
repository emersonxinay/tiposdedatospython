# metodos 

# para contar caracteres que se repiten
print("hola a todos, hola de nuevo".count("hola"))

# para convertir todo texto a mayuscula 
print("hola MUNdo de python".upper())

# para convertir todo texto a minuscula
print("HOLA minusculas ".lower())

# para convertir mayuscula solo a las iniciales de las palabras
print("hola a todos de nuevo ".title())
# para convertir unicamente a la inicial de la primera palabra de todo el texto
print("hola a Todos de nuevo ".capitalize())

# para saber cuantos caracteres tiene todo el texto
print(len("hola a todos"))

# para unir elementos separados 

print(" ".join(["chile", "peru", "argentina", "ecuador"]))


# saltos de linea 

print("hola\nmundo\n"*5)

# valores booleanos
print(True)
print(False)

